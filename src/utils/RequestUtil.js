const request = (url, method, body) => {
    let isOk;
    let fetchUrl = url;
    return new Promise((resolve, reject) => {
        fetch(fetchUrl, {
            method,
            body
        })
            .then((response) => {
                if (response.ok) {
                    isOk = true;
                } else {
                    isOk = false;
                }
                return response.json();
            })
            .then((responseData) => {
                if (isOk) {
                    resolve(responseData);
                } else {
                    reject(responseData);
                }
            })
            .catch((error) => {
                reject(error);
            });
    });
};

export default request;
